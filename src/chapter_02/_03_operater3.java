package chapter_02;

public class _03_operater3 {
    public static void main(String[] args) {
        // 비교 연산자
        System.out.println(5 > 3); // 5는 3보다 크다. 참이면 true. 거짓이면 false 가 출력됨
        System.out.println(5 >= 3); // 5는 3보다 크거나 같다.
        System.out.println(5 >= 5);
        System.out.println(5 >= 7); // false 가 출력되겠지
        System.out.println(5 == 5); // 5는 5와 같다
        System.out.println(5 != 5); // 5는 5와 같지 않다.
        System.out.println();




    }
}
